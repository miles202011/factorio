local QR_ROWS_RENDERED = (function()
    local rows = {}
    local black = "[color=1,1,1]\226\150\136[/color]"
    local white = "[color=0.13,0.10,0.07]\226\150\136[/color]"
    for _, row in ipairs(QR_DATA) do
        local buf = {}
        for j = 1, #row do
            buf[j] = row:sub(j, j) == "1" and black or white
        end
        rows[#rows + 1] = table.concat(buf)
    end
    return rows
end)()

local function add_top_button(player)
    if player and player.valid and not player.gui.top.ddz_qr_open then
        player.gui.top.add{type = "button", name = "ddz_qr_open", caption = "QQ群"}
    end
end

local function show_qr(player)
    if player.gui.screen.ddz_qr_frame then
        player.gui.screen.ddz_qr_frame.destroy()
        return
    end

    local frame = player.gui.screen.add{
        type = "frame",
        name = "ddz_qr_frame",
        caption = "QQ群 1101554578",
        direction = "vertical"
    }
    frame.auto_center = true

    local qr_flow = frame.add{type = "flow", direction = "vertical"}
    qr_flow.style.vertical_spacing = 0
    for _, line in ipairs(QR_ROWS_RENDERED) do
        for _ = 1, 2 do
            local label = qr_flow.add{type = "label", caption = line}
            label.style.font = "default-small"
            label.style.top_padding = 0
            label.style.bottom_padding = 0
            label.style.maximal_height = 6
        end
    end

    local buttons = frame.add{type = "flow", direction = "horizontal"}
    buttons.add{type = "button", name = "ddz_qr_save", caption = "导出 SVG"}
    buttons.add{type = "button", name = "ddz_qr_close", caption = "关闭"}
end

local function save_qr_svg(player)
    local scale = 6
    local size = #QR_DATA * scale
    local parts = {
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<svg xmlns="http://www.w3.org/2000/svg" width="' .. size .. '" height="' .. size .. '" viewBox="0 0 ' .. size .. ' ' .. size .. '">',
        '<rect width="' .. size .. '" height="' .. size .. '" fill="white"/>',
    }

    for i, row in ipairs(QR_DATA) do
        local y = (i - 1) * scale
        for j = 1, #row do
            if row:sub(j, j) == "1" then
                local x = (j - 1) * scale
                parts[#parts + 1] = '<rect x="' .. x .. '" y="' .. y .. '" width="' .. scale .. '" height="' .. scale .. '" fill="black"/>'
            end
        end
    end

    parts[#parts + 1] = "</svg>"
    helpers.write_file("ddz_qr.svg", table.concat(parts, "\n"), false, player.index)
    player.print("[系统] 已导出：%AppData%\\Factorio\\script-output\\ddz_qr.svg")
end

script.on_init(function()
    for _, player in pairs(game.players) do
        add_top_button(player)
    end
end)

script.on_configuration_changed(function()
    for _, player in pairs(game.players) do
        add_top_button(player)
    end
end)

script.on_event(defines.events.on_player_created, function(event)
    add_top_button(game.get_player(event.player_index))
end)

script.on_event(defines.events.on_gui_click, function(event)
    local player = game.get_player(event.player_index)
    if not player or not event.element or not event.element.valid then return end

    local name = event.element.name
    if name == "ddz_qr_open" then
        show_qr(player)
    elseif name == "ddz_qr_save" then
        save_qr_svg(player)
    elseif name == "ddz_qr_close" then
        if player.gui.screen.ddz_qr_frame then
            player.gui.screen.ddz_qr_frame.destroy()
        end
    end
end)
